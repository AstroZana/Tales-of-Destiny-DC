.func Set_BC_ID
    ;=================================
    ; Save bc id (in s2) into memory
    ; and then clear all queues
    ;=================================
    addiu sp, sp, -0x20
	sw ra, 0x1c(sp)
    sw s0, 0x18(sp)
    sw s1, 0x14(sp)
    sw s2, 0x10(sp)
    sw s3, 0xc(sp)
    
    ; store bc id in bc_id
    li s0, CURRENT_BC_ID
    sw s2, 0x0(s0)

    ;jal Clear_All_Queues
    ;nop

@@end:
    lw ra, 0x1c(sp)
    lw s0, 0x18(sp)
    lw s1, 0x14(sp)
    lw s2, 0x10(sp)
    lw s3, 0xc(sp)
    jr ra
    addiu sp, sp, 0x20
.endfunc

.func Calc_Width
    ;=================================
    ; Function to calc width to use for centering
    ; shamelessly borrowed from somewhere in the
    ; battle routine
    ;=================================
	addiu sp, sp, -0x20
	sw ra, 0x1c(sp)
    sw s0, 0x18(sp)
    sw s1, 0x14(sp)
    sw s2, 0x10(sp)
    sw s3, 0xc(sp)

    ; need to pass in A0 (textparam area)
    ; a1 is text
    ; a2 is the extra text param width info thing

    move s0, a0
    move s1, a1
    move s2, a2
    move s3, a3

    addiu a2, s2, 0xC
    jal 0x10fc90    ; sets up width for us to use
    move a3, sp   

    move a0, s0
    
    lbu a2, 0x13(s2)
    li v0, 0x80
    lhu v1, 0xc(s2)
    subu v0, v0, a2
    lh a1, 0xe(s2)
    srl v1, v1, 0x1
    sll v0, v0, 0x2
    addiu v1, v1, -0x6c00       ; pos -- x
    lh a2, 0x10(s2)
    addiu v0, v0, -0xc0
    subu a1, a1, v1
    addu a1, a1, v0
    addu a2, a2, s3      ;0x8100        ; pos -- y
    andi a1, a1, 0xffff
    jal 0x10eac0
    andi a2, a2, 0xffff

@@end:
    lw ra, 0x1c(sp)
    lw s0, 0x18(sp)
    lw s1, 0x14(sp)
    lw s2, 0x10(sp)
    lw s3, 0xc(sp)
    jr ra
    addiu sp, sp, 0x20
.endfunc

