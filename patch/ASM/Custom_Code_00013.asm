.func Display_Text_Battle_Wrapper
    ;=================================
    ; Hooked at Battle:0x003D4144
    ; Runs Every Frame
    ;=================================
	addiu sp, sp, -0x20
	sw ra, 0x1c(sp)
    
    jal 0x003d4428
    nop

    jal Process_Display_Text
    nop

@@end:
    lw ra, 0x1c(sp)
    jr ra
    addiu sp, sp, 0x20
.endfunc

.func Setup_Text_Battle_Wrapper
    ;=================================
    ; Hooked at Battle:0x003F8CF4
    ; Runs whenever a new sound starts playing
    ;=================================
	addiu sp, sp, -0x20
	sw ra, 0x1c(sp)
    sw a0, 0x18(sp)
    sw a1, 0x14(sp)
    sw a2, 0x10(sp)
    sw a3, 0xc(sp)
    ; a3 = A --> victory quote

    jal Setup_Text
    move a0, s0     ; sound buffer

    lw a0, 0x18(sp)
    lw a1, 0x14(sp)
    lw a2, 0x10(sp)
    lw a3, 0xc(sp)

    ; repeat lines removed
    dmove s1, a2
    lb v1, 0x72Ae(a0)

@@end:
    lw ra, 0x1c(sp)
    jr ra
    addiu sp, sp, 0x20
.endfunc

